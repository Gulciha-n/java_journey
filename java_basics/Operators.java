package java_basics;

public class Operators {

	public static void main(String[] args) {
		
		int x = 5 , t = 10;
		x += 10;
		
		System.out.println(x);
		System.out.println(x+t);
		System.out.println(x-t);
		System.out.println(x*t);
		System.out.println(t/x);
		
		int z = 5;
		int y = 3;
		System.out.println(z > y);  //true or false

	}

}
